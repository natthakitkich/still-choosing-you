const textEl = document.getElementById("text");
const btn = document.getElementById("nextBtn");

let index = 0;
let locked = false;

function render() {
  textEl.textContent = pages[index];
  btn.textContent = index === pages.length - 1 ? "จบแล้ว" : "ถัดไป";
}

btn.addEventListener("click", () => {
  if (locked) return;
  if (index >= pages.length - 1) return;

  locked = true;
  btn.disabled = true;

  textEl.classList.add("out");

  setTimeout(() => {
    index++;
    render();
    textEl.classList.remove("out");

    setTimeout(() => {
      locked = false;
      btn.disabled = false;
    }, 800);
  }, 800);
});

render();

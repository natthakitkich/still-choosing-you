/* แยกหน้า: 1 paragraph = 1 หน้า */
const pages = rawText
  .trim()
  .split(/\n\s*\n/);

/* ===== หน้าเปิดการ์ด ===== */
function showStart() {
  content.textContent =
`การ์ดใบนี้
เขียนถึงคนคนเดียว

กดเพื่อเปิดอ่านการ์ด`;

  startNav.classList.remove("hidden");
  pageNav.classList.add("hidden");
}

/* ===== Render page with fade ===== */
function renderPage() {
  card.classList.add("fade-out");

  setTimeout(() => {
    content.textContent = pages[pageIndex];
    card.classList.remove("fade-out");
    card.classList.add("fade-in");
  }, 250);
}

/* ===== Events ===== */
showStart();

startBtn.onclick = () => {
  pageIndex = 0;
  startNav.classList.add("hidden");
  pageNav.classList.remove("hidden");
  renderPage();
};

nextBtn.onclick = () => {
  if (pageIndex < pages.length - 1) {
    pageIndex++;
    renderPage();
  }
};

prevBtn.onclick = () => {
  if (pageIndex > 0) {
    pageIndex--;
    renderPage();
  }
};
